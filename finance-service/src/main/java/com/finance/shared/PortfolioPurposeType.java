package com.finance.shared;

/**
 * Enumeration of supported portfolio purpose type values.
 */
public enum PortfolioPurposeType {
    LONG_TERM_SAVINGS,      // Uzun vadeli birikim
    RETIREMENT,             // Emeklilik
    SHORT_TERM_TRADING,     // Kısa vadeli alım-satım
    INCOME_GENERATION,      // Gelir odaklı (temettü, kupon)
    CAPITAL_PRESERVATION,   // Sermaye koruma
    SPECULATION,            // Spekülatif
    EDUCATION_FUND,         // Eğitim fonu
    HOUSE_FUND,             // Ev alma fonu
    EMERGENCY_FUND,         // Acil durum fonu
    GENERAL                 // Genel amaçlı
}